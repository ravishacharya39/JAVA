class CalciAddSub
{
	public static void sub()
	{
		int a=10;
		int b=20;
		int c=a-b;
		System.out.println(c);
	}

	public static void multi()
	{
		int a=10;
		int b=20;
		int c=a*b;
		System.out.println(c);
	}

	public static void main(String[] args)
	{
	CalciAddSub.sub();
	CalciAddSub.multi();
	}
}
	